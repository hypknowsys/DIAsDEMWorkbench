package org.hypknowsys.misc.util;

/**
 * @version 2.1, 15 August 2003
 * @author Karsten Winkler
 */
  
public class KeyValuePair {
  
  /* ########## ########## ########## ########## ########## ######### */
  /* ########## attributes  */
  /* ########## ########## ########## ########## ########## ######### */

  protected Object Key;
  protected Object Value;

  /* ########## ########## ########## ########## ########## ######### */
  /* ########## auxiliary attributes  */
  /* ########## ########## ########## ########## ########## ######### */

  private transient StringBuffer TmpStringBuffer = null;

  /* ########## ########## ########## ########## ########## ######### */
  /* ########## constants  */
  /* ########## ########## ########## ########## ########## ######### */

  /* ########## ########## ########## ########## ########## ######### */
  /* ########## constructors  */
  /* ########## ########## ########## ########## ########## ######### */

  public KeyValuePair(Object pKey, Object pValue) {
    
    Key = pKey;
    Value = pValue;
    
  }

  /* ########## ########## ########## ########## ########## ######### */
  /* ########## accessor methods */
  /* ########## ########## ########## ########## ########## ######### */
  
  public Object getKey() {
    return Key; }
  public Object getValue() {
    return Value; }

  /* ########## ########## ########## ########## ########## ######### */
  /* ########## mutator methods */
  /* ########## ########## ########## ########## ########## ######### */

  public void setKey(Object pKey) {
    Key = pKey; }
  public void setValue(Object pValue) {
    Value = pValue; }

  /* ########## ########## ########## ########## ########## ######### */
  /* ########## standard methods */
  /* ########## ########## ########## ########## ########## ######### */

  public String toString() { 

    TmpStringBuffer = new StringBuffer(1000);
    TmpStringBuffer.append("KeyValuePair: Key=[");
    if (Key instanceof Object[]) {
      for (int i = 0; i < ( (Object[])Key ).length; i++) {
        if (i > 0) {
          TmpStringBuffer.append("; ");
        }
        TmpStringBuffer.append(( (Object[])Key )[i].toString());
      }
    }
    else {
      TmpStringBuffer.append(Key.toString());
    }
    TmpStringBuffer.append("]; Value=[");
    TmpStringBuffer.append(Value.toString());
    TmpStringBuffer.append("]");
    
    return TmpStringBuffer.toString();

  }

  /* ########## ########## ########## ########## ########## ######### */
  /* ########## interface methods */
  /* ########## ########## ########## ########## ########## ######### */
  
  /* ########## ########## ########## ########## ########## ######### */
  /* ########## public methods */
  /* ########## ########## ########## ########## ########## ######### */

  /* ########## ########## ########## ########## ########## ######### */
  /* ########## protected methods */
  /* ########## ########## ########## ########## ########## ######### */
  
  /* ########## ########## ########## ########## ########## ######### */
  /* ########## private methods */
  /* ########## ########## ########## ########## ########## ######### */
  
  /* ########## ########## ########## ########## ########## ######### */
  /* ########## static methods */
  /* ########## ########## ########## ########## ########## ######### */
  
  /* ########## ########## ########## ########## ########## ######### */
  /* ########## main method for debugging purposes  */
  /* ########## ########## ########## ########## ########## ######### */

  public static void main(String args[]) {}
  
}