  public DIAsDEMguiWelcome(GuiClient pDiasdemGui, 
    GuiClientPreferences pDiasdemGuiPreferences) {
  
    super("Welcome", true, true, false, true, 450, 300 + 15);
    
    DiasdemGui = pDiasdemGui;
    DiasdemGuiPreferences = pDiasdemGuiPreferences;
    PreferredSizeX = 450;
    PreferredSizeY = 300 + 15;
  
    URL url = getClass().getResource(
    DiasdemGuiPreferences.getIntroImageFileName());
    if (url != null) {
      ImageIcon GuiImageIcon = new ImageIcon(url);
      JLabel GuiPictureLabel = new JLabel(GuiImageIcon);
      KBorderPanel panel = new KBorderPanel(0, 0, 0, 0);
      panel.addNorth(GuiPictureLabel);
      this.getContentPane().add(panel);
    }

  }

